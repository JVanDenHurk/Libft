#include "libft.h"

void	*ft_memset(void *s, int c, int n)
{
	int i;
	char *ptr;

	i = 0;
	ptr = (char *)s;
	while (i < n)
	{
		*ptr = (char)c;
		i++;
		ptr++;
	}
	return (s);
}