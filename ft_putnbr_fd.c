#include "libft.h"

void ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", 1);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, 1);
	}
    ft_putchar_fd(n % 10 + '0', 1);
}